package com.example.domain;

public class Type {

    private String name;
    private long value;

    public void setName(String name) {
        this.name = name;
    }
    public void setValue(long value) {
        this.value = value;
    }
    public long getValue() {
        return value;
    }
    public String getName() {
        return name;
    }
}